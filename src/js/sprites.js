function getAccessorySprite(accessoryId) {
  const acc = ACCESSORIES[accessoryId];
  if (!acc) return null;
  return acc.frames.map(frame =>
    frame.map(row =>
      row.split('').map(ch => {
        if (ch === '.' || ch === ' ') return null;
        if (acc.extraColors && acc.extraColors[ch]) return acc.extraColors[ch];
        return acc.color;
      })
    )
  );
}
