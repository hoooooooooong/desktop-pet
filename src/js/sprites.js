// Helper: shorthand notation for building sprites
const SHORTHAND = {
  'b': 'body', 'e': 'belly', 'w': 'wing',
  'k': 'eyeBlack', 'W': 'eyeWhite',
  'h': 'horn', 'c': 'claw', 'm': 'mouth', 'p': 'cheek',
  'n': 'eyeBlack', 'z': 'eyeWhite',
  '.': null, ' ': null,
};

function parseSprite(grid, palette) {
  return grid.map(row => row.map(ch => {
    if (ch === '.' || ch === ' ') return null;
    const key = SHORTHAND[ch];
    if (!key) return null;
    return palette[key] || COLORS[key] || null;
  }));
}

