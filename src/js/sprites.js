function getFullPalette(dragonType) {
  const dp = DRAGON_PALETTES[dragonType] || DRAGON_PALETTES.fire;
  return {
    body: dp.body,
    belly: dp.belly,
    wing: dp.wing,
    wingTrans: dp.wingTrans,
    horn: dp.horn || COLORS.HORN,
    eyeBlack: COLORS.EYE_BLACK,
    eyeWhite: COLORS.EYE_WHITE,
    claw: COLORS.CLAW,
    mouth: COLORS.MOUTH,
    cheek: COLORS.CHEEK,
  };
}

// Parse all frames for a given dragon type
function getSprites(dragonType) {
  const palette = getFullPalette(dragonType);
  const result = {};
  for (const [action, frames] of Object.entries(SPRITE_DATA)) {
    result[action] = frames.map(frame => {
      return frame.map(row => {
        const chars = row.split('');
        return chars.map(ch => {
          if (ch === '.' || ch === ' ') return null;
          const key = SHORTHAND[ch];
          if (!key) {
            // Special fire colors
            if (ch === 'F') return COLORS.FIRE_RED;
            if (ch === 'O') return COLORS.FIRE_ORANGE;
            if (ch === 'Y') return COLORS.FIRE_YELLOW;
            if (ch === 'f') return COLORS.FIRE_CORE;
            return null;
          }
          return palette[key] || null;
        });
      });
    });
  }
  return result;
}

